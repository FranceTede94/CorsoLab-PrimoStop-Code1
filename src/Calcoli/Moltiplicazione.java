package Calcoli;

public class Moltiplicazione {
	
	private int numero5;
	private int numero6;
	
	
	public Moltiplicazione(int num1, int num2) {
		this.numero5 = num1;
		this.numero6 = num2;
	}


	public int getNumero1() {
		return numero5;
	}


	public void setNumero1(int numero1) {
		this.numero5 = numero1;
	}


	public int getNumero2() {
		return numero6;
	}


	public void setNumero2(int numero1) {
		this.numero6 = numero1;
	}
	

}
