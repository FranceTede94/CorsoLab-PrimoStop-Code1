package Calcoli;

public class Divisione {
	
	private int numero7;
	private int numero8;
	
	
	public Divisione(int num1, int num2) {
		this.numero7 = num1;
		this.numero8 = num2;
	}


	public int getNumero1() {
		return numero7;
	}


	public void setNumero1(int numero1) {
		this.numero7 = numero1;
	}


	public int getNumero2() {
		return numero8;
	}


	public void setNumero2(int numero1) {
		this.numero8 = numero1;
	}
	
	

}
