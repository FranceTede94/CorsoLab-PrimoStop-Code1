package Calcoli;

public class Sottrazione {
	
	private int numero3;
	private int numero4;
	
	public Sottrazione(int num1, int num2) {
		this.numero3 = num1;
		this.numero4 = num2;
	}


	public int getNumero1() {
		return numero3;
	}


	public void setNumero1(int numero1) {
		this.numero3 = numero1;
	}


	public int getNumero2() {
		return numero4;
	}


	public void setNumero2(int numero1) {
		this.numero4 = numero1;
	}
	
	
	

}
