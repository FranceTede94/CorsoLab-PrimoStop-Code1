package Calcoli;

public class Somma {
	
	private int numero1;
	private int numero2;
	
	
	public Somma(int num1, int num2) {
		this.numero1 = num1;
		this.numero2 = num2;
	}


	public int getNumero1() {
		return numero1;
	}


	public void setNumero1(int numero1) {
		this.numero1 = numero1;
	}


	public int getNumero2() {
		return numero2;
	}


	public void setNumero2(int numero1) {
		this.numero2 = numero1;
	}
	
	

}
